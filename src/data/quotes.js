export function getDailyQuote(dayOffset = 0) {
  try {
    const seen = JSON.parse(localStorage.getItem('mm_seen_quotes') || '[]');
    const pool = QUOTES.map((_, i) => i).filter(i => !seen.includes(i));
    const available = pool.length > 0 ? pool : QUOTES.map((_, i) => i);
    if (pool.length === 0) localStorage.setItem('mm_seen_quotes', '[]');
    const today = new Date().toDateString();
    const stored = JSON.parse(localStorage.getItem('mm_daily_quote') || '{}');
    if (stored.date === today && stored.idx !== undefined) {
      return QUOTES[stored.idx];
    }
    const idx = available[Math.floor(Math.random() * available.length)];
    const newSeen = [...(pool.length > 0 ? seen : []), idx];
    localStorage.setItem('mm_seen_quotes', JSON.stringify(newSeen));
    localStorage.setItem('mm_daily_quote', JSON.stringify({ date: today, idx }));
    return QUOTES[idx];
  } catch {
    return QUOTES[0];
  }
}
